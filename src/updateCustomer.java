    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
jTextField1.setEditable(true);
jTextField2.setEditable(true);
jTextField3.setEditable(true);
jTextField4.setEditable(true);
        DefaultListModel d1=(DefaultListModel)jList1.getModel();

        try {
            Class.forName("java.sql.DriverManager");
            Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/g_shop","root","");
            Statement stmt=(Statement) con.createStatement();
            String no=(String) jList1.getSelectedValue();
            String phone="select * from customer where c_no='"+no+"';";
            ResultSet rs=(ResultSet) stmt.executeQuery(phone);

            while (rs.next()) {
                jTextField1.setText(rs.getString("c_no"));
                jTextField2.setText(rs.getString("c_name"));
                jTextField3.setText(rs.getString("c_address"));
                jTextField4.setText(rs.getString("c_phoneno"));
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
}//GEN-LAST:event_jList1MouseClicked

