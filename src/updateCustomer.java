    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        DefaultListModel d1=(DefaultListModel)jList1.getModel();
        DefaultListModel d2=(DefaultListModel)jList2.getModel();
        d1.removeAllElements();
        d2.removeAllElements();
        try {
            Class.forName("java.sql.DriverManager");
            Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/g_shop","root","");
            Statement stmt=(Statement) con.createStatement();
            String q="Select * from customer;";
            ResultSet rs=(ResultSet)stmt.executeQuery(q);
            while(rs.next()) {
                d2.addElement(rs.getString("c_name"));
                d1.addElement(rs.getString("c_no"));

            }
            jList1.setModel(d1);
        }

        catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
}//GEN-LAST:event_jButton1ActionPerformed

